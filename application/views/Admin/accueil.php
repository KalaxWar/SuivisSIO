<h4>Comptes</h4>
<p>- 'Ajouter des Comptes' permet d'ajouter :</p>
<ul>
  <li>Des Promotions</li>
  <li>Des Professeurs</li>
  <li>Des Etudiants soit un par un ou via un fichier .CSV en UTF-8</li>
</ul>
<p>- 'Modifier des Comptes' et 'Supprimer Des Comptes' permet de modifier/supprimer :</p>
<ul>
  <li>Des Promotions</li>
  <li>Des Professeurs</li>
  <li>Des Etudiants</li>
</ul>
<p>- 'Suivis Des Promotion' permet d'afficher toutes les promotions existantes et les élèves respectifs</p>
<h4>Code d'accès</h4>
<p>Code d'accès permet de télécharger un fichier .CSV avec toute les informations importante sur les élèves de chaques promotions comme le l'IDENTIFIANT ainsi que le MOT DE PASSE de chacun</p>
<h4>Sauvegarde</h4>
<p>Un fichier est téléchargable pour sauvegarder l'intégralité de la base de données et donc ainsi pouvoir importer directement sur le client PhpMyAdmin via l'onglet 'importer'</p>
