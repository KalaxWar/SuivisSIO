<h3>Objectif</h3>
<p>Cet espace de travail est destiné au suivi individualisé de l'acquisition des compétences professionnelles du Brevet de Technicien Supérieur SIO.</p>
<h3>Fonctionnement</h3>
<p>L'étudiante ou l’étudiant saisit les informations caractérisant ses situations professionnelles puis sélectionne les activités qu’il a mobilisées.
  Pour chaque activité citée il précise, reformule et apporte sa vision personnelle des compétences qu'il pense avoir développées.
  La professeure ou le professeur Valide ou non la situation tout en pouvant laisser un commentaire.
  l'étudiante ou l’étudiant peut générer un tableau de synthèse présentant l’ensemble des situations enregistrées.
  Ce tableau de synthèse prend en compte le parcours de l'étudiant au niveau de ses activités spécifiques.
</p>
<h3>Authentification</h3>
<p>Les codes sont fourni par l'administrateur.
